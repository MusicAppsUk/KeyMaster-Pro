// coursePianoSampler.js
// =============================================================================
// KeyMaster PRO — Course-only PREMIUM piano (sampled). PROOF / INVESTIGATION.
//
// A tiny, dependency-free Web Audio sampler. It plays short piano samples and
// pitch-shifts between them to cover the Course range, giving a real-grand sound
// in place of the provisional synth tone. It is built to be a *drop-in* for the
// `courseVoice` hook foundations already references (noteOn / noteOff / cancelAll),
// matching the PianoSynth (pianoVoice) interface exactly.
//
// HARD SAFETY RULES honoured by this module:
//   • NO Tone.js, NO external library, NO remote/CDN fetch — samples are loaded
//     from the SAME ORIGIN (the repo), so there is no third-party runtime call.
//   • If samples are missing or any load fails, isReady() stays FALSE and every
//     noteOn() returns false — the caller then uses the existing, always-ready
//     pianoVoice. No silence, no delayed start, no hard dependency on samples.
//   • It NEVER touches synth.js, Scales audio, the masterclasses, or pianoVoice.js
//     (this is a separate module; pianoVoice remains the protected fallback).
//   • Course-only by intent: the integration layer only selects this voice when
//     data-view="learn"; this module itself plays nothing until asked to.
//
// Provenance/attribution lives in NOTICE and in assets/piano/salamander-lite/
// manifest.json, both generated by the build-salamander-lite GitHub Action. This
// module ships NO audio of its own; the Action writes the CC-BY Salamander
// samples (Yamaha C5, Alexander Holm) under assets/piano/salamander-lite/.
// =============================================================================

// Zone map for the first "lite" proof: Salamander notes (Yamaha C5 source,
// CC BY 3.0) every TRITONE across the Course range C3..C6. Minimal file count
// for a fast proof (max pitch-shift ±3 semitones). For higher fidelity, use the
// minor-third set (C, D#, F#, A per octave) — see SALAMANDER_MINOR_THIRD below;
// the sampler works with whatever zones actually decode.
